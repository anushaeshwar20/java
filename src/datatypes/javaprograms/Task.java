package datatypes.javaprograms;

public class Task {

	public static void main(String[] args) {
		int level=7;
		if(level==1) {
			System.out.println("Easy task");
		}
		else if(level==2) {
			System.out.println("Intermediate task");
		}
		else if(level==3) {
		System.out.println("Advance task");
		}
		else {
			System.out.println("Exit task");
		}

	}

}
